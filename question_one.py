def calculate_area(area, radius):
    output = area*radius
    print(f"The area of{area} and {radius} is = {output}")

calculate_area(3.14, 5)

 
def sum_of_numbers(area, radius, n):
    output = area+radius+n
    print(f"The sum of numbers {area} and {radius} and {n} is {output}")

sum_of_numbers(3.14, 5,4)

